import json

def generar_json(nombre_codigo_lista, nombre_archivo='clientes_data.json'):
    """
    Convierte una lista de tuplas con el nombre y el código de clientes a formato JSON y la guarda en un archivo.
    
    :param nombre_codigo_lista: Lista de tuplas con el nombre y código de los clientes.
    :param nombre_archivo: Nombre del archivo JSON (por defecto 'clientes_data.json').
    """
    # Crear el diccionario con los datos
    clientes = [{"nombre": cliente[0], "codigo": cliente[1]} for cliente in nombre_codigo_lista]

    # Guardar en un archivo JSON
    try:
        with open(nombre_archivo, 'w', encoding='utf-8') as archivo_json:
            json.dump(clientes, archivo_json, ensure_ascii=False, indent=4)
        print(f"Archivo {nombre_archivo} creado exitosamente.")
    except Exception as e:
        print(f"Hubo un error al intentar crear el archivo: {e}")
        
